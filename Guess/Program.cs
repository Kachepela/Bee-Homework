        // Если ввести не число то на консоли ничего не происходит, никакого предупреждения, никакой подсказки
        continue;
    }
    if (secret == number)
    {
        Console.WriteLine(" Поздравляем! Вы угадали. ");
        break;
    }
     if (number < 0 || number > 500) // Сообщение о том, что нужно ввести от 1 до 500, а проверка если число меньше 0. т.е. если ввести 0 то ругаться не будет
    {
        Console.WriteLine("Ошибка.Вы ввели не верное значение. Введите цифру от 1 до 500.");
    }
    else
