        Console.WriteLine("Иии...барабанная дробь\n  Вы не угадали. ");
    }
     if (secret>number)
    {
        Console.WriteLine("Загаданое число больше ,чем то что вы ввели.");
    }
     if (secret < number)
    {
        /*
         * Мелочь и всё же. Некосистентность при выводе сообщения на консоль.
